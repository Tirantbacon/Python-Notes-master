#  example of using an r or R prefix to make the string treated as a raw string,
#  which means all escape sequence codes will be ignored (such as \n and \'  ).

print(r"Wow this is prety cool.\nIt\'s making me happy! I can use all the backslashes I want! \\\\ //// ''''' """)
print(R"Wow this is also pretty cool.\nIt's making me more happy! I can use all the backslashes I want! \\\\ //// ''''' """)
