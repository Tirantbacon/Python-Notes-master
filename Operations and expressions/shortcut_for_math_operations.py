#  You can assign a value to a variable and manipulate it without having to write two of the same variables
#  in one sum
#  For example:

x = 2
x = x * 3
print(x)

#  This can be written exactly like

x = 2
x *= 3
print(x)

#  Note this is exactly like saying, x (the value of two) times 3.
#  It's a faster way than having to write x = x * 2
#  var = var operation expression becomes var operation= expression
